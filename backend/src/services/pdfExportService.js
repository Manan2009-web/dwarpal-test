function addHeader(doc, dataset, title) {
  doc.fillColor('#173449').fontSize(18).font('Helvetica-Bold').text('DwarPal', 50, 38);
  doc.fontSize(9).font('Helvetica').fillColor('#587086').text('Digital Gatepass Management System', 50, 60);
  doc.fontSize(13).font('Helvetica-Bold').fillColor('#173449').text(title, 50, 92);
  doc
    .fontSize(9)
    .font('Helvetica')
    .fillColor('#587086')
    .text(`College: ${env.collegeName}`, 50, 112)
    .text(`Generated by: ${dataset.actor?.name || 'Admin'} (${dataset.actor?.role || 'admin'})`, 50, 127)
    .text(`Generated at: ${formatDate(dataset.generatedAt)}`, 50, 142)
    .text(`Export scope: ${text(dataset.filters.exportScope)} | Partition: ${text(dataset.filters.recordPartition)}`, 50, 157);
  doc.moveTo(50, 179).lineTo(545, 179).strokeColor('#D8E2EA').lineWidth(1).stroke();
  doc.y = 192;
}

function addFooter(doc) {
  const range = doc.bufferedPageRange();
  for (let index = range.start; index < range.start + range.count; index += 1) {
    doc.switchToPage(index);
    const pageNumber = index + 1;
    doc
      .fontSize(8)
      .font('Helvetica')
      .fillColor('#7A8FA3')
      .text('Generated by DwarPal', 50, 760, { width: 220, align: 'left' })
      .text(`Page ${pageNumber} of ${range.count}`, 325, 760, { width: 220, align: 'right' });
  }
}

function ensureSpace(doc, height = 80) {
  if (doc.y + height > 730) {
    doc.addPage();
    doc.y = 50;
  }
}

function addSectionTitle(doc, title) {
  ensureSpace(doc, 32);
  doc.moveDown(0.4);
  doc.font('Helvetica-Bold').fontSize(12).fillColor('#173449').text(title);
  doc.moveDown(0.35);
}

function addFilterSummary(doc, dataset) {
  addSectionTitle(doc, 'Applied Filters');
  const filters = dataset.publicFilters || {};
  const pairs = [
    ['Report Type', filters.reportType],
    ['Record Partition', filters.recordPartition],
    ['Detail Level', filters.detailLevel],
    ['Date Range', `${filters.from ? formatDateOnly(filters.from) : 'Start'} to ${filters.to ? formatDateOnly(filters.to) : 'Now'}`],
    ['Created Date Range', `${filters.createdFrom ? formatDateOnly(filters.createdFrom) : 'Start'} to ${filters.createdTo ? formatDateOnly(filters.createdTo) : 'Now'}`],
    ['Department', filters.department],
    ['Program', filters.program],
    ['Semester', filters.semester],
    ['Role Type', filters.roleType],
    ['Status', filters.status]
  ];

  pairs.forEach(([label, value]) => {
    doc.font('Helvetica-Bold').fontSize(9).fillColor('#173449').text(`${label}: `, { continued: true });
    doc.font('Helvetica').fillColor('#587086').text(text(value));
  });
}

function addMetricCards(doc, dataset) {
  addSectionTitle(doc, 'Export Summary');
  const metrics = [
    ['User Records', dataset.userRecordCount],
    ['Detailed Records', dataset.recordCount],
    ['Approved', dataset.dashboardSummary.totalApproved],
    ['Rejected', dataset.dashboardSummary.totalRejected],
    ['Pending', dataset.dashboardSummary.totalPending],
    ['Out', dataset.dashboardSummary.totalOut],
    ['Returned', dataset.dashboardSummary.totalReturned],
    ['Leave Requests', dataset.dashboardSummary.totalLeaveRequests]
  ];
  const startX = 50;
  const cardWidth = 116;
  const cardHeight = 48;

  metrics.forEach((metric, index) => {
    const x = startX + (index % 4) * (cardWidth + 8);
    const y = doc.y + Math.floor(index / 4) * (cardHeight + 8);
    doc.roundedRect(x, y, cardWidth, cardHeight, 4).fillAndStroke('#F5F9FC', '#D8E2EA');
    doc.font('Helvetica').fontSize(8).fillColor('#587086').text(metric[0], x + 8, y + 8, { width: cardWidth - 16 });
    doc.font('Helvetica-Bold').fontSize(14).fillColor('#173449').text(String(metric[1] ?? 0), x + 8, y + 24, {
      width: cardWidth - 16
    });
  });

  doc.y += 112;
}

